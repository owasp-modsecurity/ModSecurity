/* Simple binary search */
int is_sqli_pattern(const char *key)
{
    int left = 0;
    int right = (int)patmap_sz - 1;

    while (left <= right) {
        int pos = (left + right) / 2;
        int cmp = strcmp(patmap[pos], key);
        if (cmp == 0) {
            return 1; /* TRUE */
        } else if (cmp < 0) {
            left = pos + 1;
        } else {
            right = pos - 1;
        }
    }
    return 0; /* FALSE */
}

